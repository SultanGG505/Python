# –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –∑–≤–µ–∑–¥–æ—á–∫–∏. –í—ã–≤–µ–¥–∏—Ç–µ –ø–æ–ª—É—á–µ–Ω–Ω—ã–π –º–∞—Å—Å–∏–≤ –Ω–∞ —ç–∫—Ä–∞–Ω, —Ä–∞–∑–¥–µ–ª—è—è
# —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Å—Å–∏–≤–∞ –ø—Ä–æ–±–µ–ª–∞–º–∏.
n = int(input())
# list = [ x ** 2 if x % 2 else x for x in range (1, 101)]
a = [[0] * n for i in range(n)]

for i in range(n):
    a[i][i] = 1
    a[i][n - i - 1] = 1
    a[(n // 2)][i] = 1
    a[i][(n // 2)] = 1
for row in a:
    print(' '.join(["*" if elem == 1 else "." for elem in row]))
