n, m = int(input("–í–≤–µ–¥–∏—Ç–µ N ")), int(input("–í–≤–µ–¥–∏—Ç–µ M "))
A = set()
B = set()

Both = set()
Both_sps = []
OnlyAnya = set()
OnlyAnya_sps = []
OnlyBorya = set()
OnlyBorya_sps = []

for i in range(n):
    tmp = int(input("–í–≤–µ–¥–∏—Ç–µ –Ω–æ–º–µ—Ä –∫—É–±–∏–∫–∞ –ê–Ω–∏ "))
    A.add(tmp)

